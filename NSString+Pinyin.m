NSInteger searchMatchablePinyinUnitParts(const char *pinyin)
{
    PYUnitParts *parts = parts_array;

    for (; parts->data != NULL; ++parts) {
        if (memcmp(pinyin, parts->data, strlen(parts->data)) == 0) {
            break;
        }
    }

    if (parts->data == NULL) {
        return -1;
    }

    return parts - parts_array;
}

void appendPinyinUnitParts(CharsProxy *chars_proxy, int *offset, int parts_index, PYUnitAttribute *type)
{
    PYUnitAttribute parts_type = (parts_array + parts_index)->type;

    switch (*type) { // pre-type
        case ATOM_TYPE:
        case MINOR_TYPE:
        case MAJIN_TYPE:
        case SEPERATOR_TYPE:

            if (parts_type == ATOM_TYPE || parts_type & MAJOR_TYPE) {
                ++(*offset);
                (chars_proxy + *offset)->major_index_ = parts_index;
                *type = parts_type;
            }

            break;
        case MAJOR_TYPE:

            if (parts_type & MINOR_TYPE) {
                (chars_proxy + *offset)->minor_index_ = parts_index;
                *type = MINOR_TYPE;
            } else if (parts_type & SEPERATOR_TYPE) {
                *type = SEPERATOR_TYPE;
            } else {
                ++(*offset);
                (chars_proxy + *offset)->major_index_ = parts_index;
                *type = parts_type;
            }

            break;
        default:
            break;
    }
}


@implementation NSString (Pinyin)

- (BOOL)isEmptyOrWhitespace
{
    return 0 == [self length] ||
           ![[self stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceCharacterSet]] length];
}

- (NSString *)stringByPinyinParser
{
    if ([self isEmptyOrWhitespace]) {
        return @"";
    }

    const char *ptr = [[self lowercaseString] UTF8String];
    CharsProxy CPbuf[kMaxCpLen];
    PYUnitAttribute type = ATOM_TYPE;
    NSInteger index, i, m_length;
    CharsProxy *m_CharsProxy = CPbuf;

    for (i = 0; i < kMaxCpLen; i++) {
        (m_CharsProxy + i)->major_index_ = -1;
        (m_CharsProxy + i)->minor_index_ = -1;
    }

    int offset = -1;

    while (*ptr != '\0') {
        index = searchMatchablePinyinUnitParts(ptr);

        if (index != -1) {
            appendPinyinUnitParts(m_CharsProxy, &offset, index, &type);
            ptr += strlen((parts_array + index)->data);

            if (offset == kMaxCpLen - 1) {
                break;
            }
        } else {
            ++ptr;
        }
    }

    m_length = offset + 1;
    char buf[kMaxCpLen * 8] = { 0 };
    char *pbuf = buf;

    if (m_length == 0) {
        return @"";
    }

    for (; m_length > 0; m_length--) {
        if (m_CharsProxy->major_index_ > -1) {
            memcpy(pbuf, parts_array[m_CharsProxy->major_index_].data, strlen(parts_array[m_CharsProxy->major_index_].data));
            pbuf = pbuf + strlen(parts_array[m_CharsProxy->major_index_].data);
        }

        if (m_CharsProxy->minor_index_ > -1) {
            memcpy(pbuf, parts_array[m_CharsProxy->minor_index_].data, strlen(parts_array[m_CharsProxy->minor_index_].data));
            pbuf = pbuf + strlen(parts_array[m_CharsProxy->minor_index_].data);
        } else {
            // *pbuf='*';
            // pbuf++;
        }

        *pbuf = ' ';
        pbuf++;
        m_CharsProxy++;
    }

    if (*(pbuf - 1) == ' ') {
        *(pbuf - 1) = 0;
    }

    return [NSString stringWithCString:buf encoding:NSUTF8StringEncoding];
}

