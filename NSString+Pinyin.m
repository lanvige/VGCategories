                while (*p != 0) {
                    *s++ = (*p++);
                }

                *s++ = (' ');
            } else {
                *s++ = buf[i];
            }
        } else {
            *s++ = buf[i];
        }
    }

    if (*(s - 1) == ' ') {
        s--;
    }

    return [NSString stringWithCharacters:sbuf length:s - sbuf];
}


