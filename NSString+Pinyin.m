    unichar sbuf[(length + 1) * sizeof(unichar) * 8];
    unichar *s = sbuf;

    [self getCharacters:buf range:((NSRange) { 0, length })];

    for (i = 0; i < length; i++) {
        // chinese
        if (buf[i] > 0x4dff && buf[i] < 0x9fa6) {
            pyi = pyIndex[buf[i] - 0x4e00];

            if (pyi != 999) {
                char *p = pyName[pyi];
#if 1

                if (i == 0) {
                    switch (buf[i]) {
                        case 0x8D32:
                            p = "ben";
                            break;
                        case 0x89E3:
                            p = "xie";
                            break;
                        case 0x4E50:
                            p = "yue";
                            break;
                        case 0x6C88:
                            p = "shen";
                            break;
                        case 0x5355:
                            p = "shan";
                            break;
                        case 0x4EC7:
                            p = "qiu";
                            break;
                        default:
                            break;
                    }
